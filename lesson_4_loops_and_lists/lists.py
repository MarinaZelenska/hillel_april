# ================================================
# 4. sort() — сортування списку (змінює оригінал)
# ================================================
# list.sort()              — за зростанням
# list.sort(reverse=True)  — за спаданням
# list.sort(key=func)      — за ключем

scores = [45, 92, 13, 78, 56, 100, 34]
scores.sort()
print(scores)              # [13, 34, 45, 56, 78, 92, 100]

scores.sort(reverse=True)
print(scores)              # [100, 92, 78, 56, 45, 34, 13]

# Сортування рядків за довжиною
words = ["кіт", "слон", "пес", "жираф", "їжак"]
words.sort(key=len)
print(words)   # ['кіт', 'пес', 'їжак', 'слон', 'жираф']

# ================================================
# 5. reverse() — перевертає список (змінює оригінал)
# ================================================
# list.reverse() — не сортує, просто змінює порядок на зворотній

steps = ["старт", "крок 1", "крок 2", "крок 3", "фініш"]
steps.reverse()
print(steps)   # ['фініш', 'крок 3', 'крок 2', 'крок 1', 'старт']



# ================================================
# 6. remove() — видаляє перший збіг елемента
# ================================================
# list.remove(value) — видаляє перше входження value
# Якщо елемента немає — ValueError

shopping = ["молоко", "хліб", "яйця", "хліб", "масло"]
shopping.remove("хліб")   # видаляє перший "хліб"
print(shopping)            # ['молоко', 'яйця', 'хліб', 'масло']

# Безпечне видалення:
item = "цукор"
if item in shopping:
    shopping.remove(item)
else:
    print(f"'{item}' немає у списку — нічого видаляти")

# ----- ЦІКАВИЙ ПРИКЛАД -----
# Черга в кав'ярні: перший прийшов — першим обслужили
queue = ["Іван", "Марія", "Петро", "Олена", "Сергій"]
print("\nЧерга на каву:")
while queue:
    customer = queue[0]
    queue.remove(customer)
    print(f"  Обслуговується: {customer} | Залишилось: {queue}")


# ================================================
# 7. copy() — поверхнева копія списку
# ================================================
# Важливо! Присвоєння list2 = list1 НЕ створює копію — обидві змінні
# вказують на один і той же об'єкт у пам'яті.

original = [1, 2, 3, 4, 5]
alias = original        # НЕ копія — це псевдонім!
alias.append(999)
print(original)         # [1, 2, 3, 4, 5, 999] — оригінал теж змінився!

original2 = [1, 2, 3, 4, 5]
copy1 = original2.copy()   # справжня копія
copy1.append(999)
print(original2)       # [1, 2, 3, 4, 5] — оригінал НЕ змінився
print(copy1)           # [1, 2, 3, 4, 5, 999]



