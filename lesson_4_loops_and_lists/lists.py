# 9. min() і max() — мінімум та максимум
# ================================================
# Працюють зі списками чисел і рядків (за алфавітом)

temperatures = [23, 17, 31, 8, 25, 19, 14]
print(f"\nТемператури: {temperatures}")
print(f"Найхолодніший день: {min(temperatures)}°C")
print(f"Найспекотніший день: {max(temperatures)}°C")
print(f"Різниця: {max(temperatures) - min(temperatures)}°C")

# З рядками
names = ["Зоя", "Аліса", "Микола", "Богдан"]
print(f"Перше за алфавітом: {min(names)}")   # Аліса
print(f"Останнє за алфавітом: {max(names)}")  # Зоя

