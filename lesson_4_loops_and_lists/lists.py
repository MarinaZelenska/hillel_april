# ================================================
# 10. all() і any() — перевірка умов
# ================================================
# all(iterable) → True, якщо ВСІ елементи правдиві (або список порожній)
# any(iterable) → True, якщо ХОЧА Б ОДИН елемент правдивий

ages = [18, 22, 35, 17, 29]
print(f"\nВіки: {ages}")
print(f"Всі повнолітні? {all(age >= 18 for age in ages)}")   # False (є 17)
print(f"Хтось повнолітній? {any(age >= 18 for age in ages)}") # True
