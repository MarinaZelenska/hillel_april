"""
        ЛЕКЦІЯ: TUPLES (КОРТЕЖІ)

Tuple (кортеж) — це незмінна (immutable) колекція елементів.

- Порядок зберігається
- Дозволяє дублікати
- Після створення НЕ МОЖНА змінити

tuple:
✔ незмінний
✔ швидкий
✔ використовується для фіксованих даних

namedtuple:
✔ tuple + імена полів
✔ зручний для читання
✔ альтернатива класам для простих структур
"""

t1 = (1, 2, 3)
t2 = ("a", "b", "c")

# tuple без дужок (packing)
t3 = 1, 2, 3

# важливий кейс: один елемент
t4 = (5,)   # ОБОВ'ЯЗКОВО кома!
t5 = (5)    # це вже int, а не tuple

print(type(t4))  # tuple
print(type(t5))  # int


"""
----------------------------------------
 Доступ до елементів
----------------------------------------
"""

t = (10, 20, 30, 40)

print(t[0])     # 10
print(t[-1])    # 40
print(t[1:3])   # (20, 30)


"""
----------------------------------------
Tuple — НЕЗМІННИЙ
----------------------------------------
"""

t = (1, 2, 3)

# t[0] = 100

# Але якщо всередині список — його можна змінити
t = (1, [2, 3])
t[1].append(4)
print(t)  # (1, [2, 3, 4])


"""
----------------------------------------
Основні операції
----------------------------------------
"""

t = (1, 2, 3)

print(len(t))        # довжина
print(2 in t)        # True
print(t.count(2))    # скільки разів зустрічається
print(t.index(3))    # індекс елемента

# конкатенація
t_new = t + (4, 5)

# множення
t_repeat = t * 2


"""
----------------------------------------
Розпаковка (UNPACKING)
----------------------------------------
"""

t = (10, 20, 30)

a, b, c = t
print(a, b, c)

# зірочка (важливо!)
a, *middle, c = (1, 2, 3, 4, 5)
print(a)       # 1
print(middle)  # [2, 3, 4]
print(c)       # 5


