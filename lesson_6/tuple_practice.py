"""
 Що це?

namedtuple — це tuple з іменованими полями.

➡ дозволяє звертатись до значень як до атрибутів (як у класі)

----------------------------------------
Як створити
----------------------------------------
"""

from collections import namedtuple

User = namedtuple("User", ["name", "age", "city"])

user1 = User("Anna", 25, "Kyiv")

print(user1.name)   # Anna
print(user1.age)    # 25
print(user1.city)   # Kyiv


