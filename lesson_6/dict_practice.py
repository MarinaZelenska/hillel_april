"""
📌 Перевірка ключа
"""
user = {"name": "Anna"}

print("name" in user)  # True

"""
📌 dict comprehension
"""

nums = [1, 2, 3]

squares = {x: x**2 for x in nums}
print(squares)  # {1:1, 2:4, 3:9}



