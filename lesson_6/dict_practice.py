"""
========================================
        ЛЕКЦІЯ: СЛОВНИКИ (DICTIONARY)
========================================

Що таке dict?

Dictionary (словник) — це структура даних у форматі:
ключ -> значення

✔ невпорядкований (до Python 3.7 був, зараз зберігає порядок)
✔ змінний (mutable)
✔ ключі унікальні
✔ ключі — тільки immutable типи (str, int, tuple)

----------------------------------------
Як створити словник
----------------------------------------
"""

# класичний варіант
d = {"name": "Anna", "age": 25}

# через dict()
d2 = dict(name="John", age=30)

print(d)
print(d2)


"""
----------------------------------------
Доступ до значень
----------------------------------------
"""

user = {"name": "Anna", "age": 25}

print(user["name"])      # Anna

# безпечний доступ
print(user.get("age"))        # 25
print(user.get("city"))       # None
print(user.get("city", "Kyiv"))  # значення за замовчуванням


"""
----------------------------------------
Додавання та зміна
----------------------------------------
"""

user = {"name": "Anna"}

# додати
user["age"] = 25

# змінити
user["name"] = "Maria"

print(user)


"""
----------------------------------------
Видалення
----------------------------------------
"""

user = {"name": "Anna", "age": 25}

user.pop("age")   # видаляє ключ
# del user["name"]

print(user)


"""
----------------------------------------
Основні методи
----------------------------------------
"""

user = {"name": "Anna", "age": 25}

print(user.keys())    # ключі
print(user.values())  # значення
print(user.items())   # пари (ключ, значення)


"""
========================================
        ЦИКЛИ В СЛОВНИКАХ
========================================
"""

user = {"name": "Anna", "age": 25, "city": "Kyiv"}

"""
 1. Перебір ключів
"""
for key in user:
    print(key)

"""
2. Перебір значень
"""
for value in user.values():
    print(value)

"""
3. Перебір ключ + значення
"""
for key, value in user.items():
    print(key, value)


"""
----------------------------------------
Приклад з логікою
----------------------------------------
"""

# порахувати суму значень
prices = {"apple": 10, "banana": 5, "orange": 8}

total = 0
for price in prices.values():
    total += price

print("Сума:", total)


"""
----------------------------------------
Вкладені словники
----------------------------------------
"""

users = {
    "user1": {"name": "Anna", "age": 25},
    "user2": {"name": "John", "age": 30}
}

print(users["user1"]["name"])  # Anna


